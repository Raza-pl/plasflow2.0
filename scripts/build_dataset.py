# ── I/O helpers ───────────────────────────────────────────────────────────────


def _open_fasta(path: Path):
    """Return a SeqIO iterator for plain, gzipped, or bz2-compressed FASTA."""
    name = path.name
    if name.endswith(".gz"):
        return SeqIO.parse(gzip.open(path, "rt"), "fasta")
    if name.endswith(".bz2"):
        return SeqIO.parse(bz2.open(path, "rt"), "fasta")
    return SeqIO.parse(str(path), "fasta")


def _fasta_files_in_dir(directory: Path, recursive: bool = False) -> list[Path]:
    """Return all FASTA files (by extension) in *directory*.

    Args:
        directory: Root directory to search.
        recursive: If True, search subdirectories too (rglob).
    """
    files = []
    glob_fn = directory.rglob if recursive else directory.glob
    for ext in _FASTA_EXTS:
        for p in sorted(glob_fn(f"*{ext}")):
            if p.is_file():
                files.append(p)
    return sorted(set(files))


# ── Sequence loading ──────────────────────────────────────────────────────────


def load_and_subsample(
    path: Path,
    label: str,
    max_per_class: int,
    min_length: int = 1000,
    seed: int = 42,
) -> tuple[list[str], list[str], list[int]]:
    """Load sequences from a single FASTA, filter, subsample, assign label.

    Args:
        path: FASTA file (plain, .gz, or .bz2).
        label: Class name — must be a key in CLASS_TO_IDX.
        max_per_class: Maximum number of sequences to keep.
        min_length: Minimum sequence length in bp.
        seed: RNG seed for reproducibility.

    Returns:
        Tuple of (sequences, seq_ids, labels) as parallel lists.
    """
    rng = random.Random(seed)
    all_seqs: list[tuple[str, str]] = []
    for rec in _open_fasta(path):
        seq = str(rec.seq).upper()
        if len(seq) >= min_length and set(seq) <= {"A", "C", "G", "T", "N"}:
            all_seqs.append((rec.id, seq))

    if len(all_seqs) > max_per_class:
        all_seqs = rng.sample(all_seqs, max_per_class)

    label_idx = CLASS_TO_IDX[label]
    ids = [s[0] for s in all_seqs]
    seqs = [s[1] for s in all_seqs]
    labels = [label_idx] * len(seqs)

    logger.info(
        "  %-40s  kept %6d  (min_len=%d  cap=%d)",
        path.name,
        len(seqs),
        min_length,
        max_per_class,
    )
    return seqs, ids, labels


def load_from_dir(
    directory: Path,
    label: str,
    max_total: int,
    min_length: int = 1000,
    seed: int = 42,
) -> tuple[list[str], list[str], list[int]]:
    """Load and merge sequences from all FASTA files in *directory*.

    Files are loaded in sorted order; a global cap of *max_total* sequences is
    applied after merging (random subsample if over cap).

    Args:
        directory: Directory containing FASTA files.
        label: Class name for all sequences in this directory.
        max_total: Total sequence cap across all files.
        min_length: Minimum sequence length in bp.
        seed: RNG seed.

    Returns:
        Tuple of (sequences, seq_ids, labels) as parallel lists.
    """
    files = _fasta_files_in_dir(directory, recursive=True)
    if not files:
        logger.warning("No FASTA files found in %s", directory)
        return [], [], []

    logger.info("Loading '%s' from directory: %s  (%d files)", label, directory, len(files))
    all_seqs: list[tuple[str, str]] = []
    for fpath in files:
        for rec in _open_fasta(fpath):
            seq = str(rec.seq).upper()
            if len(seq) >= min_length and set(seq) <= {"A", "C", "G", "T", "N"}:
                all_seqs.append((rec.id, seq))
        logger.info("  %-40s  running total: %d", fpath.name, len(all_seqs))

    rng = random.Random(seed)
    if len(all_seqs) > max_total:
        logger.info("  Subsampling %d → %d (cap=%d)", len(all_seqs), max_total, max_total)
        all_seqs = rng.sample(all_seqs, max_total)

    label_idx = CLASS_TO_IDX[label]
    ids = [s[0] for s in all_seqs]
    seqs = [s[1] for s in all_seqs]
    labels = [label_idx] * len(seqs)

    logger.info("  → '%s' total: %d sequences from %d files", label, len(seqs), len(files))
    return seqs, ids, labels


# ── Fragmentation ─────────────────────────────────────────────────────────────


def fragment_sequences(
    seqs: list[str],
    ids: list[str],
    window_sizes: tuple[int, ...] = (1000, 2000, 5000, 10_000),
    step_fraction: float = 0.5,
    max_fragments: int | None = None,
    seed: int = 42,
) -> tuple[list[str], list[str]]:
    """Tile complete sequences into contig-sized windows.

    Used to convert complete RefSeq chromosomes (4–7 Mb) into realistically
    sized training fragments that match what the classifier sees at inference
    time (assembled metagenomic contigs, 1–50 kb).

    NOT used for metagenomic assembly inputs — those are already contig-length.

    Args:
        seqs: Complete DNA sequences (e.g. full bacterial chromosomes).
        ids: Sequence identifiers (same order as seqs).
        window_sizes: Window lengths in bp to produce per sequence.
        step_fraction: Step = window × step_fraction (0.5 = 50% overlap).
        max_fragments: Cap on total fragments (random downsample if exceeded).
        seed: RNG seed for downsampling.

    Returns:
        Tuple of (fragment_seqs, fragment_ids).
    """
    frag_seqs: list[str] = []
    frag_ids: list[str] = []

    for parent_id, seq in zip(ids, seqs, strict=True):
        for w in window_sizes:
            if w > len(seq):
                continue
            step = max(1, int(w * step_fraction))
            for start in range(0, len(seq) - w + 1, step):
                fragment = seq[start : start + w]
                if set(fragment) <= {"A", "C", "G", "T", "N"}:
                    frag_seqs.append(fragment)
                    frag_ids.append(f"{parent_id}_w{w}_s{start}")

    if max_fragments is not None and len(frag_seqs) > max_fragments:
        rng = random.Random(seed)
        indices = rng.sample(range(len(frag_seqs)), max_fragments)
        frag_seqs = [frag_seqs[i] for i in indices]
        frag_ids = [frag_ids[i] for i in indices]

    logger.info("  Fragmentation → %d contig windows", len(frag_seqs))
    return frag_seqs, frag_ids


def load_chrom_dir(
    directory: Path,
    max_fragments: int,
    min_length: int = 1000,
    window_sizes: tuple[int, ...] = (1000, 2000, 5000, 10_000),
    step_fraction: float = 0.5,
    seed: int = 42,
) -> tuple[list[str], list[str], list[int]]:
    """Load complete chromosome FASTAs from *directory*, tile into windows.

    Each .fna file in *directory* is a RefSeq assembly (one or more complete
    chromosome/chromid sequences).  All sequences are tiled with
    :func:`fragment_sequences` and a global cap of *max_fragments* is applied.

    Args:
        directory: Directory with per-assembly .fna files (from
            download_refseq_chromosomes.py).
        max_fragments: Total contig window cap across all genomes.
        min_length: Minimum bp for a whole chromosome to be included.
        window_sizes, step_fraction, seed: Passed to fragment_sequences.

    Returns:
        Tuple of (fragment_seqs, fragment_ids, labels).
    """
    files = _fasta_files_in_dir(directory)
    if not files:
        logger.warning("No FASTA files found in chromosome directory: %s", directory)
        return [], [], []

    logger.info("Loading chromosomes from %s  (%d assembly files)", directory, len(files))
    all_chrom_seqs: list[str] = []
    all_chrom_ids: list[str] = []

    for fpath in files:
        for rec in _open_fasta(fpath):
            seq = str(rec.seq).upper()
            if len(seq) >= min_length:
                all_chrom_seqs.append(seq)
                all_chrom_ids.append(rec.id)

    logger.info(
        "  Loaded %d chromosome sequences from %d assemblies", len(all_chrom_seqs), len(files)
    )

    if not all_chrom_seqs:
        return [], [], []

    frag_seqs, frag_ids = fragment_sequences(
        all_chrom_seqs,
        all_chrom_ids,
        window_sizes=window_sizes,
        step_fraction=step_fraction,
        max_fragments=max_fragments,
        seed=seed,
    )
    chr_label = CLASS_TO_IDX["chromosome"]
    return frag_seqs, frag_ids, [chr_label] * len(frag_seqs)


def load_metagenome_dir(
    directory: Path,
    max_total: int,
    min_length: int = 1000,
    seed: int = 42,
) -> tuple[list[str], list[str], list[int]]:
    """Load metagenomic assembly contigs from *directory* as chromosome examples.

    Metagenomic assemblies are already contig-length (output of assemblers like
    MEGAHIT or metaSPAdes) — no windowing is applied.  All files in the
    directory are merged and subsampled to *max_total*.

    Labeled as 'chromosome' (majority-label assumption: >95% of metagenomic
    contigs are chromosomal fragments; the small plasmid/phage minority adds
    acceptable label noise and improves real-world generalisation).

    Expected files (from download_metagenome_assemblies.py):
        clinical_metagenome.fna
        environmental_metagenome.fna
        human_gut_metagenome.fna

    Args:
        directory: Directory with metagenome FASTA files.
        max_total: Total contig cap across all files.
        min_length: Minimum contig length in bp.
        seed: RNG seed.

    Returns:
        Tuple of (sequences, seq_ids, labels).
    """
    files = _fasta_files_in_dir(directory)
    if not files:
        logger.warning("No FASTA files found in metagenome directory: %s", directory)
        return [], [], []

    logger.info("Loading metagenome contigs from %s  (%d files)", directory, len(files))
    all_seqs: list[tuple[str, str]] = []
    per_file_counts: dict[str, int] = {}

    for fpath in files:
        count_before = len(all_seqs)
        for rec in _open_fasta(fpath):
            seq = str(rec.seq).upper()
            if len(seq) >= min_length and set(seq) <= {"A", "C", "G", "T", "N"}:
                all_seqs.append((rec.id, seq))
        per_file_counts[fpath.name] = len(all_seqs) - count_before
        logger.info("  %-45s  %7d contigs", fpath.name, per_file_counts[fpath.name])

    rng = random.Random(seed)
    if len(all_seqs) > max_total:
        logger.info("  Subsampling metagenome contigs %d → %d", len(all_seqs), max_total)
        all_seqs = rng.sample(all_seqs, max_total)

    chr_label = CLASS_TO_IDX["chromosome"]
    ids = [s[0] for s in all_seqs]
    seqs = [s[1] for s in all_seqs]
    labels = [chr_label] * len(seqs)

    logger.info("  → metagenome total: %d contigs (labeled 'chromosome')", len(seqs))
    return seqs, ids, labels


# ── Streaming windowed loader (memory-efficient) ──────────────────────────────


def load_windowed_streaming(
    files: list[Path],
    label: str,
    max_total: int,
    min_length: int = 1000,
    window_sizes: tuple[int, ...] = (1000, 2000, 5000, 10_000),
    step_fraction: float = 0.5,
    seed: int = 42,
) -> tuple[list[str], list[str], list[int]]:
    """Stream FASTA files, window each sequence immediately, reservoir-sample to max_total.

    Memory footprint: max_total × max_window_size  (e.g. 75 K × 10 kb = 750 MB)
    instead of total_database_size (e.g. 12.8 GB for PLSDB + RefSeq plasmids).

    The full source sequence is held in memory only while its windows are being
    generated — it is then garbage-collected before the next record is read.

    Algorithm: Vitter's Algorithm R reservoir sampling.  Each window seen so far
    has equal probability of being in the final reservoir, so the sample is
    uniformly random even though the total window count is not known in advance.

    Args:
        files: FASTA files to stream (merged into one pool in order).
        label: Class label — must be a key in CLASS_TO_IDX.
        max_total: Reservoir size = maximum windows returned.
        min_length: Source sequences shorter than this are skipped entirely.
        window_sizes: Tile each source sequence at these lengths.
        step_fraction: Step = window × step_fraction  (0.5 = 50 % overlap).
        seed: RNG seed for reservoir sampling.

    Returns:
        (sequences, seq_ids, labels) as parallel lists, length ≤ max_total.
    """
    rng = random.Random(seed)
    reservoir: list[tuple[str, str]] = []  # (seq_id, fragment)
    n_seen = 0  # total windows considered so far (for reservoir probability)

    for fpath in files:
        logger.info("  Streaming %s …", fpath.name)
        n_file = 0
        for rec in _open_fasta(fpath):
            seq = str(rec.seq).upper()
            if len(seq) < min_length:
                continue
            # Generate and immediately reservoir-sample windows for this record.
            # The full `seq` string is referenced only inside this inner loop;
            # once the loop exits the GC can reclaim it.
            for w in window_sizes:
                if w > len(seq):
                    continue
                step = max(1, int(w * step_fraction))
                for start in range(0, len(seq) - w + 1, step):
                    fragment = seq[start : start + w]
                    n_seen += 1
                    n_file += 1
                    if len(reservoir) < max_total:
                        reservoir.append((f"{rec.id}_w{w}_s{start}", fragment))
                    else:
                        # Replace a random existing element with probability
                        # max_total / n_seen — preserves uniform sampling.
                        j = rng.randint(0, n_seen - 1)
                        if j < max_total:
                            reservoir[j] = (f"{rec.id}_w{w}_s{start}", fragment)
        logger.info(
            "    %s: %d source windows generated  (reservoir now %d)",
            fpath.name,
            n_file,
            len(reservoir),
        )

    label_idx = CLASS_TO_IDX[label]
    ids = [r[0] for r in reservoir]
    seqs = [r[1] for r in reservoir]
    labels_list = [label_idx] * len(seqs)
    logger.info(
        "  → '%s' total: %d windows (from %d total windows seen, cap=%d)",
        label,
        len(seqs),
        n_seen,
        max_total,
    )
    return seqs, ids, labels_list


# ── Legacy chromosome download ────────────────────────────────────────────────


def download_chromosomes(
    accessions: list[str],
    out_path: Path,
    batch_size: int = 5,
) -> Path:
    """Fetch RefSeq chromosome sequences via NCBI Entrez and save as FASTA.

    Legacy method — used when neither --chrom-dir nor a pre-existing
    chromosomes.fna is available.  For proper 10K-genome training use
    download_refseq_chromosomes.py instead.
    """
    if out_path.exists():
        logger.info("Chromosome FASTA already exists at %s — skipping download", out_path)
        return out_path

    out_path.parent.mkdir(parents=True, exist_ok=True)
    total_written = 0

    with open(out_path, "w") as fh:
        for i in range(0, len(accessions), batch_size):
            batch = accessions[i : i + batch_size]
            logger.info(
                "Fetching chromosomes %d–%d / %d: %s",
                i + 1,
                min(i + batch_size, len(accessions)),
                len(accessions),
                ", ".join(batch),
            )
            try:
                handle = Entrez.efetch(
                    db="nucleotide",
                    id=",".join(batch),
                    rettype="fasta",
                    retmode="text",
                )
                fh.write(handle.read())
                handle.close()
                total_written += len(batch)
            except Exception as exc:
                logger.warning("Failed to fetch batch %s: %s", batch, exc)
            time.sleep(0.4)

    logger.info("Downloaded %d chromosome sequences to %s", total_written, out_path)
    return out_path


